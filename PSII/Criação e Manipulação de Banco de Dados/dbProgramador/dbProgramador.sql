SELECT e.id_empregado, e.id_departamento, d.localizacao 
	FROM empregado e, departamento d
		WHERE e.id_departamento = d.id_departamento;
        
SELECT e.id_empregado, e.id_departamento, d.localizacao 
	FROM empregado e JOIN departamento d 
		ON e.id_departamento = d.id_departamento;
        
SELECT e.id_empregado, e.id_departamento, d.localizacao
	FROM empregado e, departamento d
		WHERE e.id_departamento = d.id_departamento AND e.nm_empregado LIKE 'KING';

SELECT e.nm_empregado, e.id_departamento, d.nm_departamento, d.localizacao
	FROM empregado e RIGHT JOIN departamento d 
		ON (e.id_departamento = d.id_departamento);
        
SELECT e.id_empregado, e.nm_empregado, g.id_grade_salarial
