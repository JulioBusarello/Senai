SELECT DISTINCT nm_departamento, lider
	FROM empregado e, departamento d
		WHERE d.id_departamento = e.id_departamento;
        
SELECT * 
	FROM empregado
		ORDER BY nm_empregado;
        
SELECT * FROM empregado
	ORDER BY nm_empregado
		LIMIT 1, 1;
        
SELECT *, (salario + coalesce(comissao, 0)) 'TOTAL' 
	FROM empregado
		ORDER BY nm_empregado;
        
SELECT *, CASE 
	WHEN salario > 4000 THEN 'GANHA BEM'
    WHEN salario > 2500 THEN 'GANHA MAIS OU MENOS'
    ELSE 'QUER ESMOLA?'
END 'STATUS'
	FROM empregado;
    
